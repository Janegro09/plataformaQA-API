// Incluimos controladores, modelos, schemas y modulos
const mongoose = require('mongoose');
const Schema   = mongoose.Schema;

var Tokens = new Schema({
    userId: {
        type: Object,
        required: true
    },
    sessionId: {
        type: String,
        unique: true,
        required: true
    },
    token: {
        type: String,
        required: true
    },
    updatedAt: {
        type: Date,
        default: Date.now
    }
});

module.exports = mongoose.model('Tokens',Tokens);