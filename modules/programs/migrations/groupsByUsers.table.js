// Incluimos controladores, modelos, schemas y modulos
const mongoose = require('mongoose');
const Schema   = mongoose.Schema;

var groupsByUsers = new Schema({
    groupId: {
        type: String,
        required: true
    },
    userGroupId: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});

module.exports = mongoose.model('groupsByUsers',groupsByUsers);